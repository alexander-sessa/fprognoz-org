Любительская ассоциация долгое время существовавшая в сети Беларусбанка.<br />
С 2008 г. ФП SBN начала использовать для проведения турниров наш сервер.<br />
В 2011 г. из игроков SBN была восстановлена ФП-ассоциация Белоруссии.<br />
В 2012-13 гг. под флагом SBN был проведен пробный турнир для проверки работы нововведений на сервере.
