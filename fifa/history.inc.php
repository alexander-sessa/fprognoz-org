    <font color="#800080" face="sans-serif" size="2">(&quot;Сошо&quot;, Франция) </font></strong>
    <font face="sans-serif" size="2">и </font><strong><font color="#808000" face="sans-serif" size="2">Георгию Гусеву </font>
    <font color="#800080" face="sans-serif" size="2">(&quot;Алания&quot;, Россия)</font></strong>
    <font face="sans-serif" size="2">.</font>
    <br />
    <font face="sans-serif" size="2">&nbsp;&nbsp; Также был проведен 1-й розыгрыш Мировой Лиги - чемпионата
    мира среди сборных лиг. Первым чемпионом мира стала сборная </font>
    <font color="#800080" face="sans-serif" size="2"><strong>Белоруссии</strong></font>
