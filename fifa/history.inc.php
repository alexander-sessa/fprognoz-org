<p align="center"><font size="3" color="#FF0000"><strong><em>--------------- Сезон 1997 ---------------</em></strong></font></p>
<p align="justify"><font face="sans-serif" size="2">&nbsp;&nbsp;&nbsp;&nbsp; Осенью
    1997 года был организован 1-й чемпионат Украины по ФутболПрогнозу. В нем приняли участие 16 команд.
    Они были вначале разбиты на 4 подгруппы, а затем продолжили борьбу по кубковой системе. Первым
    чемпионом Украины стала команда </font>
    <strong><font face="sans-serif" size="2" color="#800000">&quot;Шахтер&quot;(Макеевка)</font></strong>
    под руководством <font color="#808000"><strong>Димки Запорожца</strong></font></p>

<p align="center"><font size="3" color="#FF0000"><strong><em>--------------- Сезон 1998 ---------------</em></strong></font></p>
<p align="justify"><font face="sans-serif" size="2">&nbsp;&nbsp;&nbsp;&nbsp; Весной
    1998 года стартовал 2-й чемпионат Украины, а вскоре и 1-й чемпионат России. В Украине уже было
    организовано 2 лиги по 16 команд и разыгран 1-й Кубок, а в России 48 команд разыграли укороченный
    чемпионат по смешанной системе. Чемпионом Украины стала команда </font>
    <font color="#800000"><strong>ФК&quot;Черкассы&quot;</strong></font>
    под руководством <font color="#808000"><strong>Владимира Иванова</strong></font>, а чемпионом России - команда
    <strong><font color="#808000">Евгения Бойцова</font><font color="#800000">
    &quot;Локомотив-2&quot;(Санкт-Петербург)</font></strong></p>

<p align="center"><font size="3" color="#FF0000"><strong><em>--------------- Сезон 1998/99 ---------------</em></strong></font></p>
<p align="justify"><font face="sans-serif" size="2">&nbsp;&nbsp; Сезон 1998/99
    стал первым полноценным европейским сезоном Футбол-Прогноза. Были разыграны 12 чемпионатов и
    первые ЕвроКубки. Все ЕвроКубки выиграли украинские команды - </font>
    <font color="#800080" face="sans-serif" size="2"><strong>ФК&quot;Черкассы&quot;,
    &quot;Десна&quot;, &quot;Днепр&quot; и &quot;Металлург&quot;М.</strong></font>
    <br />
    <font face="sans-serif" size="2">&nbsp;&nbsp;Настоящим героем евро-сезона стал </font>
    <font color="#808000" face="sans-serif" size="2"><strong>Сергей Мышеловка</strong></font>
    <font face="sans-serif" size="2">, выигравший 2 чемпионата - Украины и Италии, сделав при этом в
    чемпионате Италии &quot;дубль&quot;, выиграв чемпионат и &quot;Золотую Бутсу&quot; Италии командой </font>
    <font color="#800080" face="sans-serif" size="2"><strong>&quot;Фоджа&quot;</strong></font>
    <font face="sans-serif" size="2">. Аналогичный &quot;дубль&quot; удался еще двум игрокам - </font>
