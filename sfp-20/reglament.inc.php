    1. ОБЩАЯ ИНФОРМАЦИЯ

  1.1. Историческая справка.

В сентябре 1997 года начался первый турнир по футбольному прогнозированию в фидо-конференции UA.FOOTBALL.
Весной 1998-го параллельно со вторым сезоном украинского турнира по тем же правилам был проведен турнир в
конференции RU.SPORT.FOOTBALL.
В августе того же года организаторами вышеупомянутых турниров была создана конференция любителей футбольного
прогнозирования SU.FOOTBALL.PROGNOZ.
С 2007-го назад турниры SFP стали проводится на программном обеспечении нашего сайта fprognoz.org.

  1.2. Особенности турнира

1.2.1. В межсайтовом турнире используется схема прогнозирования, широко известная под названиями "Футбольный
Ринг", "В девятку" или, собственно, "Судоку".
Схема древняя: клеточки на бумаге заполняли задолго до появления интернета.
В интернете турниры по этой схеме первым, с 1998 года, начал проводить ФК "Форвард".
Там система игры получила название "Футбольный ринг".
Бои на "ринге" сейчас проходят на нескольких футбол-прогнозных сайтах, так что прогнозирование девятью цифрами
знакомо многим игрокам.

1.2.2. При организации нашего турнира хотелось внести улучшения в известную схему: сделать игру более
командной - уйти от примитивных поединков на линиях с их "хоккейными" счетами, и более приближенной к футболу.
Для этого используется идея перемещения мяча по полю, используемая в турнирах PREDвидение.

1.2.3. На выбор количества матчей в туре (6) повлиял успешный турнир VIVA-sudoku (primegang.ru).
Оттуда же взята идея замен между таймами, которая вносит дополнительный игровой момент для тренеров и позволяет
принять участие в матче большему количеству игроков.
В 2017-м году был по этим правилам был проведен турнир сборных сайта fprognoz.org <a href="https://fprognoz.org/?a=world&s=2017&m=text&ref=news">"Мировая Лига 2017"</a>.
Успех завершившего турнира подтвердил жизнеспособность схемы и вдохновил на проведение юбилейного межсайтового
турнира.

1.2.4. Сайт fprognoz.org использует технологии автоматической публикации поступиших прогнозов, показа
результатов и итогов тура в реальном времени.
Возможно, Вы успели опробовать их на турнире ProfiOpen 2017.

1.2.5. Два способа подачи прогнозов: через сайт и по e-mail.
Прогнозы могут подаваться как в индивидуальном порядке, так и тренером команды за других игроков своей команды.

1.2.6. Удобный интерфейс тренера управления составом команды.
Есть возможность симуляции результатов матча при внесении замен.

1.2.7. После каждого тура формируется статистика по игрокам и командам.

1.2.8. Нет "халяве": вместо отсутствующих игроков основного состава играют случайно-сгенерированные прогнозы
из заблаговременно опубликованного списка.

