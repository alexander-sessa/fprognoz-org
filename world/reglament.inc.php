    1. ОБЩАЯ ИНФОРМАЦИЯ

  1.1. В турнире используется схема прогнозирования, широко известная под названиями "Футбольный Ринг",
"В девятку" или, собственно, "Судоку".
Схема древняя: клеточки на бумаге заполняли задолго до появления интернета.
В интернете турниры по этой схеме первым, с 1998 года, начал проводить ФК "Форвард".
Там система игры получила название "Футбольный ринг".
Бои на "ринге" сейчас проходят на нескольких футбол-прогнозных сайтах, так что прогнозирование девятью цифрами
знакомо многим игрокам.

  1.2. В известную схему проведения турниров внесено изменение с целью сделать игру более командной - уйти от
поединков на линиях с их "хоккейными" счетами, и более приближенной к футболу.
Для этого используется идея перемещения мяча по полю, используемая в турнирах PREDвидение.

  1.3. На выбор количества матчей в туре (6) повлиял успешный турнир VIVA-sudoku (primegang.ru).
Оттуда же взята идея замен между таймами, которая вносит дополнительный игровой момент для тренеров и позволяет
принять участие в матче большему количеству игроков.
В 2017-м году был по этим правилам был проведен турнир сборных сайта fprognoz.org "Мировая Лига 2017".
Успех завершившего турнира подтвердил жизнеспособность схемы и вдохновил на проведение в начале 2018 г.
юбилейного межсайтового турнира "SFP - 20 лет!" с участием 16 сборных.


