<p>1.5.
В случае если две или более команд набрали одинаковое количество очков, то места определяются по
следующим критериям. Выше располагается команда, у которой:</p>
<p>- Больше очков без учета бонусов</p>
<p>- Лучше наивысшие место. При равенстве смотрим второе и т.д.</p>
