<p>1.2. Порядок заявки команд в турниры Серии</p>
<p>1.2.1.
Любая команда может выслать заявку для участия в любом турнире Серии.
Организатор конкретного турнира рассматривает заявку, и высылает РЕШЕНИЕ о
включении или отказе (с указанием причины). К участию могут быть не допущены
команды,&nbsp; имеющие на своем счету исключения из командных турниров из-за неявок
или снятие с турниров. Включение таких команд производится по специальному
решению организаторов, которое формулирует условия участия таких команд.</p>
<p>1.2.2.
Заявка в турнир возможна, если вы согласны с данными правилами и согласны с их
незначительными изменениями, направленными на их оптимизацию. Все команды,
допущенные к участию в турнире, берут на себя обязательство доиграть турнир до
конца. В случае выбывания по ходу турнира на команду накладывается
дисквалификация.</p>
<p>1.2.3.
Изменения в составах допускаются не позднее, чем за 3 суток до старта 1-го тура.</p>
<p>1.2.4.
Клубы/команды/сборные (далее клубы) допускаются для участия в турнирах &quot;Серии”
по одному из следующих условий:</p>
<ul>
  <li>приглашение организаторов;</li>
  <li>участие в прошедших турнирах Серии в любом из официальных сезонов;</li>
  <li>единогласное решение организаторов Серии (для новичков);</li>
</ul>
