<p>Если после предварительного этапа, команды, не прошедшие в финал, не разыгрывают места с
n-ного и командам ставится что они
заняли n-m место, для определения набранных очков складываются очки за места, начиная с n
и заканчивая m, и делятся на количество складываемых мест.</p>
<p><u>Например</u>:
команда заняла 21-24 место. Набранные очки = (12+11+10+9)/4=10,5.</p>
<p>1.3.2.
Бонус за сыгранные матчи и набранные очки.</p>
<p>Бонус рассчитывается по формуле:&nbsp; Бонус = 10 * О / МО,</p>
<p>где:</p>
<p>О – Набранные командой очки,</p>
<p>МО – Максимально возможное количество очков которое могла набрать команда (и * на количество очков за победу).</p>
<p>Бонус округляется до десятых.<br>
Бонус считается при условии: 2 очка за победу, 1 – за ничью, 0 – за поражение.</p>
<p>Очки за место и Бонус складываются и получаются очки команды за турнир.</p>
