<h5>Общая информация о «Мастер-Серии»</h5>
<p>«Мастер-серия» – это серия турниров по футбольному прогнозированию, целью
которой является выявление сильнейшей сборной сезона. По результатам выступления
в каждом турнире сборные команды в зависимости от итогов своей игры получают
баллы, на основе которых формируется рейтинг в текущем сезоне Мастер-Серии.
Сборная, имеющая максимальный рейтинг по окончанию всех турниров, объявляется
<strong>чемпионом «Мастер-Серии».</strong></p>
<p>Организатором «Мастер-Серии» является управляющий совет, который состоит из
людей - организаторов турниров Серии. Все решения по проведению «Мастер-Серии»
(регламент, учитывая состав и общие принципы проведения турниров, прием в совет
новых участников, исключение из совета действующих участников и т.д.)
принимаются управляющим советом Мастер-Серии вместе с приглашенным экспертом
А.Митрофановым (всего 7 человек). Совет принимает решения по предложению,
поставленному на голосование одним из членов совета. Вопрос должен иметь два
варианта ответа: за и против предложение (например, за нововведение или против,
за дисквалификацию или против и т.д.). Предложение члена совета принимается,
если за него проголосовало большинство из семи человек.</p>
